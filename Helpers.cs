namespace cs_console_programs
{
    public static class Helpers
    {
        public static int Add(int num1, int num2)
        {
            int result = num1 + num2;
            return result;
        }
        public static int Subtract(int num1, int num2)
        {
            int result = num1 - num2;
            return result;
        }
    }
}
